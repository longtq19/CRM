/** Tra nhãn tiếng Việt theo mã quyền (CODE) — dùng khi DB lưu name dạng permission.* hoặc tiếng Anh. */
function dictionaryByPermissionCode(code: string): string | undefined {
  if (!code) return undefined;
  if (DICTIONARY[code]) return DICTIONARY[code];
  const upper = code.toUpperCase();
  if (DICTIONARY[upper]) return DICTIONARY[upper];
  for (const [k, v] of Object.entries(DICTIONARY)) {
    if (k.toUpperCase() === upper) return v;
  }
  return undefined;
}

/** Hiển thị chức năng hệ thống: ưu tiên map theo `code`, sau đó `translate(name)`. */
export const translatePermissionLabel = (code: string, name?: string | null): string => {
  const fromCode = dictionaryByPermissionCode(code);
  if (fromCode) return fromCode;
  return translate(name || code);
};

/** Tooltip Nhóm quyền: ưu tiên mô tả từ API (`permissions.description`), fallback nhãn + mã. */
export const permissionTooltip = (
  code: string,
  name: string | undefined | null,
  description?: string | null
): string => {
  const d = description?.trim();
  if (d) return d;
  return `${translatePermissionLabel(code, name)} — mã ${code}.`;
};

function resolveI18nKey(value: string): string | undefined {
  if (I18N_KEYS[value]) return I18N_KEYS[value];
  const lower = value.toLowerCase();
  for (const [k, v] of Object.entries(I18N_KEYS)) {
    if (k.toLowerCase() === lower) return v;
  }
  return undefined;
}

export const translate = (value: string | undefined | null): string => {
  if (!value) return '';

  const i18n = resolveI18nKey(value);
  if (i18n) return i18n;

  // DB lưu name dạng permission.view_logs — ưu tiên map sang code VIEW_LOGS rồi tra DICTIONARY (tiếng Việt).
  const permKeyMatch = value.match(/^permission\.(.+)$/i);
  if (permKeyMatch) {
    const tail = permKeyMatch[1];
    const codeKey = tail.toUpperCase();
    if (DICTIONARY[codeKey]) {
      return DICTIONARY[codeKey];
    }
    const lowerCode = codeKey.toLowerCase();
    for (const [key, translation] of Object.entries(DICTIONARY)) {
      if (key.toLowerCase() === lowerCode) {
        return translation;
      }
    }
    const words = tail.split('_').map((w) => w.charAt(0).toUpperCase() + w.slice(1));
    return `Quyền: ${words.join(' ')}`;
  }

  // Check if exact match in dictionary
  if (DICTIONARY[value]) {
    return DICTIONARY[value];
  }
  
  // Check if should keep original
  if (KEEP_ORIGINAL.includes(value)) {
    return value;
  }
  
  // Try case-insensitive match
  const lowerValue = value.toLowerCase();
  for (const [key, translation] of Object.entries(DICTIONARY)) {
    if (key.toLowerCase() === lowerValue) {
      return translation;
    }
  }
  
  return value;
};
